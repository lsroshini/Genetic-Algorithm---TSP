        "import streamlit as st\n",
        "import numpy as np\n",
        "import pandas as pd\n",
        "import matplotlib.pyplot as plt\n",
        "import random\n",
        "import os\n",
        "import gym\n",
        "from stable_baselines3 import PPO\n",
        "\n",
        "# Fix: Use os.getcwd() in Streamlit instead of __file__\n",
        "current_dir = os.getcwd()\n",
        "orders_path = os.path.join(current_dir, 'orders.csv')\n",
        "shelves_path = os.path.join(current_dir, 'shelves.csv')\n",
        "robots_path = os.path.join(current_dir, 'robots.csv')\n",
        "\n",
        "# Load data\n",
        "orders_df = pd.read_csv(orders_path)\n",
        "shelves_df = pd.read_csv(shelves_path)\n",
        "robots_df = pd.read_csv(robots_path)\n",
        "\n",
        "# Define the Distance Matrix\n",
        "shelves_locations = shelves_df[['Location_X', 'Location_Y']].to_numpy()\n",
        "num_shelves = len(shelves_locations)\n",
        "distance_matrix = np.zeros((num_shelves, num_shelves))\n",
        "\n",
        "def shelf_distance(s1, s2):\n",
        "    return np.linalg.norm(shelves_locations[s1] - shelves_locations[s2])\n",
        "\n",
        "for i in range(num_shelves):\n",
        "    for j in range(num_shelves):\n",
        "        distance_matrix[i][j] = shelf_distance(i, j)\n",
        "\n",
        "# --- Gym Environment ---\n",
        "class WarehouseEnv(gym.Env):\n",
        "    def __init__(self, robot_id, assigned_orders):\n",
        "        super(WarehouseEnv, self).__init__()\n",
        "        self.robot_id = robot_id\n",
        "        self.assigned_orders = assigned_orders\n",
        "        self.action_space = gym.spaces.Discrete(num_shelves)\n",
        "        self.observation_space = gym.spaces.Box(low=0, high=100, shape=(2,), dtype=np.float32)\n",
        "\n",
        "    def reset(self):\n",
        "        self.state = np.zeros(2)\n",
        "        return self.state\n",
        "\n",
        "    def step(self, action):\n",
        "        distance_to_shelf = shelf_distance(int(self.state[0]), action)\n",
        "        reward = -distance_to_shelf\n",
        "        self.state[0] = action\n",
        "        return self.state, reward, False, {}\n",
        "\n",
        "# --- Streamlit UI ---\n",
        "def main():\n",
        "    st.title(\"Warehouse Optimization with ACO and PPO\")\n",
        "\n",
        "    if st.button(\"Start Optimization\"):\n",
        "        # Assign Orders to Robots\n",
        "        num_robots = len(robots_df)\n",
        "        orders_to_robots = {i: [] for i in range(num_robots)}\n",
        "        for _, order in orders_df.iterrows():\n",
        "            assigned_robot = random.choice(range(num_robots))\n",
        "            orders_to_robots[assigned_robot].append(order)\n",
        "\n",
        "        # Train RL Models\n",
        "        models = {}\n",
        "        for robot_id, assigned_orders in orders_to_robots.items():\n",
        "            env = WarehouseEnv(robot_id, assigned_orders)\n",
        "            model = PPO(\"MlpPolicy\", env, verbose=0)\n",
        "            model.learn(total_timesteps=3000)  # Reduce for faster execution\n",
        "            models[robot_id] = model\n",
        "\n",
        "        # **User selects a robot to visualize**\n",
        "        selected_robot = st.selectbox(\"Select a Robot:\", list(orders_to_robots.keys()))\n",
        "\n",
        "        # **Plot only the selected robot‚Äôs route**\n",
        "        if selected_robot in orders_to_robots:\n",
        "            fig, ax = plt.subplots(figsize=(8, 6))\n",
        "            for order in orders_to_robots[selected_robot]:\n",
        "                shelves_to_pick = np.random.choice(range(len(shelves_df)), size=min(order['Number_of_Items'], len(shelves_df)), replace=False)\n",
        "                colors = plt.cm.viridis(np.linspace(0, 1, len(shelves_to_pick) - 1))\n",
        "                for i in range(len(shelves_to_pick) - 1):\n",
        "                    x1, y1 = shelves_df[['Location_X', 'Location_Y']].iloc[shelves_to_pick[i]].values\n",
        "                    x2, y2 = shelves_df[['Location_X', 'Location_Y']].iloc[shelves_to_pick[i + 1]].values\n",
        "                    ax.plot([x1, x2], [y1, y2], color=colors[i], marker='o', label=f'Task {i + 1}')\n",
        "                ax.set_title(f'Path Taken by Robot {selected_robot + 1}')\n",
        "                ax.set_xlabel('Location X')\n",
        "                ax.set_ylabel('Location Y')\n",
        "                ax.legend(loc='best')\n",
        "            st.pyplot(fig)\n",
        "\n",
        "if __name__ == \"__main__\":\n",
        "    main()\n"
